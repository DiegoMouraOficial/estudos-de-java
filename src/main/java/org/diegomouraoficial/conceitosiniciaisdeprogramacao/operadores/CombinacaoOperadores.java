package org.diegomouraoficial.conceitosiniciaisdeprogramacao.operadores;

public class CombinacaoOperadores {
    public static void main(String[] args) {
        int a = 5;
        int b = 8;
        int c = 3;
        int d = 16;
        int e = 25;

        a += 1; // a = a + 1
        b -= 2; // b = b - 2
        c *= 3; // c = c * 3
        d /= 4; // d = d / 4
        e %= 5; // e = e % 5

        System.out.println(" Resultado de a: " + a);
        System.out.println(" Resultado de b: " + b);
        System.out.println(" Resultado de c: " + c);
        System.out.println(" Resultado de d: " + d);
        System.out.println(" Resultado de e: " + e);




    }
}
