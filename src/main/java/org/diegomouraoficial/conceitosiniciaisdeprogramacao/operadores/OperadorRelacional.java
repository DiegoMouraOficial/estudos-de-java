package org.diegomouraoficial.conceitosiniciaisdeprogramacao.operadores;

public class OperadorRelacional {
    public static void main(String[] args) {
        int a = 10;
        int b = 2;

        System.out.println(" Igual: " + (a == b));
        System.out.println(" Diferente: " + (a != b));
        System.out.println(" Maior que: " + (a > b));
        System.out.println(" Maior ou igual a: " + (a >= b));
        System.out.println(" Menor que: " + (a < b));
        System.out.println(" Menor ou igual a: " + (a <= b));


    }
}
