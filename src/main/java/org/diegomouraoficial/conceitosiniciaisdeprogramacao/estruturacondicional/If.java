/*
*   if(<expressao logica>) {
*       instrucoes a serem executadas caso a <expressao logica> seja verdadeira.
*   }
*
* */

public class If {
    public static void main(String[] args) {

        int idade = 18;

        if(idade >= 18) {
            System.out.println(" Você é maior de idade.");
        }
    }
}
